<h2>Big Fires start with the tiniest Sparks</h2>

<p>~take it easy when doing tickets~.</p>

<p>Why?</p>

<p>
	Its the project managers job to ensure deadlines are met. Its your responsibility to maintain the
	codebase, to ensure the quality of written code, to make sure edgecases are handled, to think
	about how this piece of code fits into the bigger picture. It is also your responsibility to
	communicate these aspects to your project managers. Constantly!
</p>

<p>
	I recently had a ticket that included the following quote: "...a quick solution beats a clean
	one...". Now, it might seem like im nitpicking about this tiny detail in a otherwise decently well
	written ticket. But this is not just a minor issue. It's a spark of something horrific. It's the
	equivilent of pandoras box of software development. If left unchecked, this will become a fire
	that burns like hell. It's a systemic issue, it will rot your codebase's to the core and eat you
	and your teams motivation alive.
</p>

<p>
	~"...When I started programming as a kid, I didn't know people were paid to program. Even as I
